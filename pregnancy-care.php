                    <div class="entry-content">
                      <p class="mb-30"><font size="3"  color="black">A high-risk pregnancy might pose challenges before, during or after delivery. If you have a high-risk pregnancy, you and your baby might need special monitoring or care throughout your pregnancy. Understand what causes a high-risk pregnancy, and what you can do to take care of yourself and your baby.<br>
                     </font></p>

                            <div class="col-md-12 mb-30">
                             <h4 class="entry-title"><a href="#">What are the risk factors for a high-risk pregnancy?</a></h4>
                            <p class="mb-30"><font size="3"  color="black">Sometimes a high-risk pregnancy is the result of a medical condition present before pregnancy. In other cases, a medical condition that develops during pregnancy for either mom or baby causes a pregnancy to become high risk.<br>Specific factors that might contribute to a high-risk pregnancy include:</font></p>

                        <ul class="list-icon theme-colored">
                              <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black"><b>Advanced maternal age: </b>
                               Pregnancy risks are higher for mothers age 35 and older.</font>
                              </li>
                               <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black"><b>Lifestyle choices :  </b>
                              Smoking cigarettes, drinking alcohol and using illegal drugs can put a pregnancy at risk.</font>
                              </li>
                               <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black"><b>Medical history :  </b>
                               A prior C-section, low birth weight baby or preterm birth — birth before 37 weeks of pregnancy — might increase the risk in subsequent pregnancies. Other risk factors include a family history of genetic conditions, a history of pregnancy loss or the death of a baby shortly after birth.</font>
                              </li>
                               <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black"><b>Underlying conditions : </b>
                               Chronic conditions — such as diabetes, high blood pressure and epilepsy — increase pregnancy risks. A blood condition, such as anemia, an infection or an underlying mental health condition also can increase pregnancy risks.</font>
                              </li>
                              <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black"><b>Pregnancy complications :  </b>
                              Various complications that develop during pregnancy pose risks, such as problems with the uterus, cervix or placenta. Other concerns might include too much amniotic fluid (polyhydramnios) or low amniotic fluid (oligohydramnios), restricted fetal growth, or Rh (rhesus) sensitization — a potentially serious condition that can occur when your blood group is Rh negative and your baby's blood group is Rh positive.</font>
                              </li>
                              <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black"><b>Multiple pregnancy : </b>
                            Pregnancy risks are higher for women carrying twins or higher order multiples.</font>
                              </li>
                        </ul>
                      </div>
                        <div class="col-md-12 mb-30">
                         <h4 class="entry-title"><a href="#">What steps can I take to promote a healthy pregnancy?</a></h4>
                          <p class="mb-30"><font size="3"  color="black">Whether you know ahead of time that you'll have a high-risk pregnancy or you simply want to do whatever you can to prevent a high-risk pregnancy, stick to the basics. For example:</font>
                          </p>
   
                        <ul class="list-icon theme-colored">
                            <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black"><b>Schedule a preconception appointment: </b>
                           If you're thinking about becoming pregnant, consult your health care provider. He or she might counsel you to start taking a daily prenatal vitamin and reach a healthy weight before you become pregnant. If you have a medical condition, your treatment might need to be adjusted to prepare for pregnancy. Your health care provider might also discuss your risk of having a baby with a genetic condition.</font>
                          </li>
                           <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black"><b>Be cautious when using assisted reproductive technology (ART) :</b>
                           If you're planning to use ART to get pregnant, consider how many embryos will be implanted. Multiple pregnancies carry a higher risk of preterm labor..</font>
                          </li>
                           <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black"><b>Seek regular prenatal care :</b>
                            Prenatal visits can help your health care provider monitor your health and your baby's health. Depending on the circumstances, you might be referred to a specialist in maternal-fetal medicine, genetics, pediatrics or other areas.</font>
                          </li>
                           <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black"><b>Miscarriage  </b>
                           The rate of miscarriage for women who conceive using IVF with fresh embryos is similar to that of women who conceive naturally — about 15 to 25 percent — but the rate increases with maternal age. Use of frozen embryos during IVF, however, may slightly increase the risk of miscarriage.</font>
                          </li>
                             <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black"><b>Eat a healthy diet:</b>
                           During pregnancy, you'll need more folic acid, calcium, iron and other essential nutrients. A daily prenatal vitamin can help fill any gaps. Consult your health care provider if you have special nutrition needs due to a health condition, such as diabetes.</font>
                          </li>
                          <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black"><b>Gain weight wisely : </b>
                           Gaining the right amount of weight can support your baby's health — and make it easier to shed the extra pounds after delivery. Work with your health care provider to determine what's right for you.</font>
                          </li>
                          <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black"><b>Avoid risky substances : </b>
                           If you smoke, quit. Alcohol and illegal drugs are off-limits, too. Get your health care provider's OK before you start — or stop — taking any medications or supplements.</font>
                          </li>       
                        </ul>
                     </div>

                      <div class="col-md-12 mb-30">
                        <h4 class="entry-title"><a href="#">Do I need special tests?</a></h4>
                        <p class="mb-30"><font size="3"  color="black">If you have a high-risk pregnancy, you might consider various tests or procedures in addition to routine prenatal screening tests. Depending on the circumstances, your health care provider might recommend:</font></p>

                        <ul class="list-icon theme-colored">
                          <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black"><b>Specialized or targeted ultrasound :</b> This type of fetal ultrasound — an imaging technique that uses high-frequency sound waves to produce images of a baby in the uterus — targets a suspected problem, such as abnormal development. </font>
                          </li>
                           <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black"><b>Amniocentesis : </b>During this procedure, a sample of the fluid that surrounds and protects a baby during pregnancy (amniotic fluid) is withdrawn from the uterus. Typically done after week 15 of pregnancy, amniocentesis can identify certain genetic conditions, as well as neural tube defects — serious abnormalities of the brain or spinal cord.</font>
                          </li>
                          <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black"><b>Chorionic villus sampling (CVS) :</b>During this procedure, a sample of cells is removed from the placenta. Typically done between weeks 10 and 12 of pregnancy, CVS can identify certain genetic conditions.</font>
                          </li>
                          <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black"><b>Cordocentesis : </b>This test, also known as percutaneous umbilical blood sampling, is a highly specialized prenatal test in which a fetal blood sample is removed from the umbilical cord. Typically done after week 18 of pregnancy, the test can identify chromosomal conditions, blood disorders and infections.</font>
                          </li>
                          <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black"><b>Cervical length measurement : </b> Your health care provider might use an ultrasound to measure the length of your cervix at prenatal appointments to determine if you're at risk of preterm labor.</font>
                          </li>
                          <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black"><b>Lab tests :  </b> Your health care provider might take a swab of your vaginal secretions to check for fetal fibronectin — a substance that acts like a glue between the fetal sac and the lining of the uterus. The presence of fetal fibronectin might be a sign of preterm labor.</font>
                          </li>
                          <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black"><b>Biophysical profile :  </b>  This prenatal test is used to check on a baby's well-being. The test combines fetal heart rate monitoring (nonstress test) and fetal ultrasound.
                          Some prenatal diagnostic tests — such as amniocentesis and chorionic villus sampling — carry a small risk of pregnancy loss. Ultimately, the decision to pursue prenatal testing is up to you and your partner. Discuss the risks and benefits with your health care provider.</font>
                          </li>
                      </ul>
                  </div>
                     <div class="col-md-12 mb-30">
                        <h4 class="entry-title"><a href="#">Can I have a planned home birth?</a></h4>
                        <p class="mb-30"><font size="3"  color="black">Home birth generally isn't recommended for high-risk pregnancies. It depends on why your pregnancy has been deemed high risk, however. During your prenatal care, your health care provider will review a list of medical conditions during pregnancy and labor that would compromise the safety of a planned home birth.<br>

                        Your health care provider might caution against a planned home birth if:</font></p>
                        <ul class="list-icon theme-colored">
                          <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black">You have diabetes, high blood pressure, a seizure disorder or any chronic medical condition</font>
                          </li>
                           <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black">You develop a pregnancy complication, such as preeclampsia, preterm labor or significant anemia.</font>
                          </li>
                          <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black">You're pregnant with multiples.</font>
                          </li>
                            <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black">Your baby doesn't settle into a position that allows for a headfirst delivery.</font>
                          </li>
                        </ul>
     
                     </div>

                     <div class="col-md-12 mb-30">
                         <h4 class="entry-title"><a href="#">What can I do to ease my anxiety?</a></h4>
                    
                           <p class="mb-30"><font size="3"  color="black">If you have a high-risk pregnancy, you might feel scared or anxious about your pregnancy. You might be reluctant to think about the future and nervous about prenatal visits — for fear that you'll hear bad news.
                           <br>
                          Unfortunately, anxiety can affect your health and your baby's health. Consult your health care provider about healthy ways to relax and stay calm. Some studies suggest that certain techniques — such as imagining pleasant objects or experiences or listening to music — can reduce anxiety during pregnancy.</font></p>

                          <h4 class="entry-title"><a href="#">What else do I need to know about high-risk pregnancy?</a></h4>
                    
                           <p class="mb-30"><font size="3"  color="black">Consult your health care provider about how to manage any medical conditions you might have during your pregnancy and how your health might affect labor and delivery. Ask your health care provider to discuss specific signs or symptoms to look out for, such as:</font></p>
                             <ul class="list-icon theme-colored">
                          <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black">Vaginal bleeding.</font>
                          </li>
                           <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black">Persistent headaches.</font>
                          </li>
                          <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black">Pain or cramping in the lower abdomen.</font>
                          </li>
                           <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black">Regular or frequent contractions — a tightening sensation in the abdomen.</font>
                          </li>
                           <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black">Decreased fetal activity.</font>
                          </li>
                           <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black">Pain or burning with urination.</font>
                          </li>
                          <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black">Changes in vision, including blurred vision.</font>
                          </li>
                         
                         
                        </ul>
                             
                        <p class="mb-30"><font size="3"  color="black">Also, find out which signs or symptoms should prompt you to contact your health care provider and when to seek emergency care.<br>

                        A high-risk pregnancy might have ups and downs. Do your best to stay positive as you take steps to promote a healthy pregnancy.</font></p>
                                                 
                    
