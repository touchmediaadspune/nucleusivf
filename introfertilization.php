                   <h4 class="entry-title"><a href="#">What you can expect :</a></h4>
                   <p class="mb-30"><font size="3" color="black">In vitro fertilization (IVF) involves several steps — ovulation induction, egg retrieval, sperm retrieval, fertilization and embryo transfer. One cycle of IVF can take about two weeks, and more than one cycle may be required.</font></p>

                   <p class="mb-30"><font size="3" color="black"><b>Ovulation induction :</b> If you're using your own eggs during IVF, at the start of a cycle you'll begin treatment with synthetic hormones to stimulate your ovaries to produce multiple eggs — rather than the single egg that normally develops each month. Multiple eggs are needed because some eggs won't fertilize or develop normally after fertilization. You may need several different medications, Such As:</font></p>
                   <ul class="list-icon theme-colored">
                    <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black">
                      <b> Medications for ovarian stimulation :</b> To stimulate your ovaries, you might receive an injectable medication containing a follicle-stimulating hormone (FSH), a luteinizing hormone (LH) or a combination of both. These medications stimulate more than one egg to develop at a time.</font>
                    </li>
                     <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black">
                      <b> Medications for oocyte maturation :</b>When the follicles are ready for egg retrieval — generally after eight to 14 days — you will take human chorionic gonadotropin (HCG) or other medications to help the eggs mature.</font>
                    </li>
                     <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black">
                      <b>Medications to prevent premature ovulation :</b>   These medications prevent your body from releasing the developing eggs too soon.</font>
                    </li>
