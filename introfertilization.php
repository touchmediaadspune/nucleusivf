                    </li>
                     <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black">
                      <b>Ovarian hyperstimulation syndrome : </b>   Use of injectable fertility drugs, such as human chorionic gonadotropin (HCG), to induce ovulation can cause ovarian hyperstimulation syndrome, in which your ovaries become swollen and painful.<br>Signs and symptoms typically last a week and include mild abdominal pain, bloating, nausea, vomiting and diarrhea. If you become pregnant, however, your symptoms might last several weeks. Rarely, it's possible to develop a more-severe form of ovarian hyperstimulation syndrome that can also cause rapid weight gain and shortness of breath.</font>
                    </li>
                     <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black">
