                    </li>
                     <li><i class="fa fa-pencil-square-o"></i><font size="3" color="black">
                      <b>  Egg-retrieval procedure complications : </b> Use of an aspirating needle to collect eggs could possibly cause bleeding, infection or damage to the bowel, bladder or a blood vessel. Risks are also associated with general anesthesia, if used.</font>
                    </li>
